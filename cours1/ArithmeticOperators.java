public class ArithmeticOperators {

    public static void main(String[] args) {
        float x = 10f, y = 4f;
        int a = 10, b = 3;
        float result;

        result = x + y; // addition
        System.out.println("x + y = " + result);

        result = x - y; // soustraction
        System.out.println("x - y = " + result);

        result = x * y; // multiplication
        System.out.println("x * y = " + result);

        result = x / y; // division
        System.out.println("x / y = " + result);

        result = a % b; // Module
        System.out.println("a % b  = " + result);
    }
}
